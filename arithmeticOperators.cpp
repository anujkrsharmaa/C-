#include<iostream>
using namespace std;
int main(){
    int x = 10 , y = 5;
    cout<<x + y << endl; // Addition
    cout<<x - y << endl; // Subtraction     
    cout<<x * y << endl; // Multiplication
    cout<<x / y << endl; // Division
    cout<<x % y << endl; // Modulus
    // Increment and Decrement Operators
    cout<<x++ << endl; // Post-increment
    cout<<++x << endl; // Pre-increment
    cout<<x-- << endl; // Post-decrement
    cout<<--x << endl; // Pre-decrement
    cout<<y++ << endl; // Post-increment on y
    cout<<++y << endl; // Pre-increment on y
    cout<<y-- << endl; // Post-decrement on y
    cout<<--y << endl; // Pre-decrement on y
    return 0; // Indicate that the program ended successfully       
}