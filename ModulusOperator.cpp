#include<iostream>
using namespace std;
int main() {
    int x = 10; // Example value for x
    int y = 2; // Example value for y
    int r = x + y; // Addition
    int s = x - y; // Subtraction
    int m = x * y; // Multiplication
    int d = x / y; // Division
    int mod = x % y; // Modulus
    cout << "Addition: " << r << endl; // Output the result of addition
    cout << "Subtraction: " << s << endl; // Output the result of subtraction
    cout << "Multiplication: " << m << endl; // Output the result of multiplication
    cout << "Division: " << d << endl; // Output the result of division     
    cout << "Modulus: " << mod << endl; // Output the result of modulus
    return 0; // Indicate that the program ended successfully
}